    //% block
    export function findWinner(): mp.Player {
        let winner = mp.getPlayerByNumber(1)
        let highestScore = -1
        let maxPlayers = mp.allPlayers().length

        for (let i = 1; i <= maxPlayers; i++) {
            let p = mp.getPlayerByNumber(i)

            if (mp.getPlayerSprite(p)) {
                let currentScore = mp.getPlayerProperty(p, MultiplayerState.score)

                if (currentScore > highestScore) {
                    highestScore = currentScore
                    winner = p
                }
            }
        }
        return winner
    }

